      SUBROUTINE ORGMCW(ISP,D,H,JOSTND,IMODTY,IRTN,CW,DEBUG)
      IMPLICIT NONE
C----------
C ORGANON $Id: orgmcw.f $
C----------
C
C  CALLED FROM BASE MODEL SUBROUTINE CWIDTH TO COMPUTE CROWN WIDTH
C  VALUES USING ORGANON EQUATIONS WHEN THE USER HAS CALLED FOR THIS
C  USING THE ORGMCW KEYWORD.
C----------
COMMONS
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'ORGANON.F77'
C
COMMONS
C----------
C VARIABLE DECLARATIONS:
C----------
      LOGICAL DEBUG
C
      INTEGER JOSTND,IMODTY,IORGSP,IORGSG,IRTN,ISP
C
      REAL    CW,D,H
C-----------
      CW = 0.
C----------
C  IF USER ENTERED THE ORGMCW KEYWORD, THEN COMPUTE CROWN WIDTHS
C  USING THE ORGANON EQUATIONS. OTHERWISE RETURN
C----------
      IF(LORGCW) THEN
C----------
C  CONVERT THE FVS SPECIES SEQUENCE NUMBER TO A VALID ORGANON SPECIES
C  AND GET THE SPECIES ORGANON SPECIES GROUP FOR THE VALID ORGANON 
C  SPECIES. (E.G. FOR THE OC VARIANT ISP = 1 TRANSLATES TO IORGSP = 242
C  AND IORGSG = 7).
C----------
        CALL ORGSPC(ISP,IORGSP,IORGSG)
C----------
C  CALL THE APPROPRIATE MAXIMUM CROWN WIDTH ROUTINE FOR THIS MODEL TYPE.
C----------
        SELECT CASE (IMODTY)
        CASE (1)
          CALL MCW_SWO(IORGSG,D,H,CW)
        CASE (2)
          CALL MCW_NWO(IORGSG,D,H,CW)
        CASE (3)
          CALL MCW_SMC(IORGSG,D,H,CW)
        END SELECT
C
        IRTN = 1
        IF(DEBUG)WRITE(JOSTND,*)' IN ORGMCW IMODTY,ISP,IORGSP,IORGSG,',
     &  'D,H,CW= ',IMODTY,ISP,IORGSP,IORGSG,D,H,CW
      ENDIF
C
      IF(DEBUG)WRITE(JOSTND,*)' LEAVING ORGMCW IRTN= ',IRTN
      RETURN
      END

